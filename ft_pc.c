#include "printf.h"

void	ft_pc(char *str, int j, int *s)
{
	int *l;

	l = ft_loop(j, str, 0);
	if (l[0] == 1)
		write(1, "%", 1);
	if (l[1] == 1 && l[0] != 1)
		ft_spaces(l[3] - 1, 2);
	else
		ft_spaces(l[3] - 1, 1);
	if (l[0] != 1)
		write(1, "%", 1);
	if (l[3] > 0)
		*s += l[3];
	else
		*s += 1;
	ft_memdel((void*)(&l));
}
